def glyph(*rows: str) -> tuple[str, ...]:
    if len(rows) != 8:
        raise ValueError("glyphs must have exactly 8 rows")
    for row in rows:
        if len(row) != 4:
            raise ValueError(f"bad row width in {rows!r}")
    return tuple(rows)


